    private int obtener() {
        if (combo.getSelectedIndex() != 0) {
            Seleccionar(combo.getSelectedItem().toString());
            jLabel3.setVisible(true);
            jLabel4.setVisible(true);
            lblnombre.setVisible(true);
            lblpass.setVisible(true);
            jButton2.setVisible(true);
            jButton3.setVisible(true);
            jSeparator1.setVisible(true);
            return combo.getSelectedIndex();
        }
        return 0;
    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        aprobados.add(new Aprobados(lblnombre.getText(), lblpass.getText(), new Estado("00:00","0","0")));
        JOptionPane.showMessageDialog(null, "¡Se ha aprobado la solicitud!");
        jLabel3.setVisible(false);
        jLabel4.setVisible(false);
        lblnombre.setVisible(false);
        lblpass.setVisible(false);
        jButton2.setVisible(false);
        jButton3.setVisible(false);
        jSeparator1.setVisible(false);
        if (pos != 0) {
            solicitudes.remove(pos - 1);
        }
        combo.removeAllItems();
        combo.addItem("Seleccionar");
        for (Solicitud sol : solicitudes) {
            combo.addItem(sol.getNombre());
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        if (pos != 0) {
            solicitudes.remove(pos - 1);
            jLabel3.setVisible(false);
            jLabel4.setVisible(false);
            lblnombre.setVisible(false);
            lblpass.setVisible(false);
            jButton2.setVisible(false);
            jButton3.setVisible(false);
            jSeparator1.setVisible(false);
            JOptionPane.showMessageDialog(null, "¡Solicitud rechazada!");
            combo.removeAllItems();
            combo.addItem("Seleccionar");
            for (Solicitud sol : solicitudes) {
                combo.addItem(sol.getNombre());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

