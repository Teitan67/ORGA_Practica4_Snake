    public void Seleccionar(String nombre) {
        for (Solicitud sol : solicitudes) {
            if (sol.getNombre().equalsIgnoreCase(nombre)) {
                lblnombre.setText(nombre);
                lblpass.setText(sol.getPassword());
                return;
            }
        }
    }

